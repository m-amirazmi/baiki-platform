export enum MenuItem {
  Dashboard = "Dashboard",
  Users = "Users",
}

export const menu = [
  { title: MenuItem.Dashboard, href: "/" },
  { title: MenuItem.Users, href: "/users" },
];

export const getPageTitle = (path: string) => {
  switch (menu.find((item) => item.href === path)?.title) {
    case MenuItem.Dashboard:
      return "Dashboard";
    case MenuItem.Users:
      return "Users Management";
    default:
      return "Baiki RMS";
  }
};

